#include <stdio.h>

int main()
{
    printf("Olá, mundo!\n");  // O comando "\n" faz uma quebra de linha.

    printf("Olá, povo! ");  // Neste caso ele escreve na mesma linha, sem quebrar linha.

    return (0);
}

